package package5;

public class UcBasamakliSayilar {
	
	public static void main(String[] args) {
		
	
	for (int i = 0; i<=100; i++) {

		if (i % 15 == 0) {
			System.out.print(i + " bu sayilar 15 e tam bolunebiliyor");
			
			System.out.println("===================");
		} 	
		else if (i % 5 == 0) {
			System.out.println("bu sayilar 5 e tam bolunuyor");
			

			System.out.println("===================");

		}
	}
}

}


